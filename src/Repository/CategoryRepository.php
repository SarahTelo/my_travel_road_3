class CategoryRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Category::class);
    }

    /**
     * Filtres: voyages par visibilité et par catégorie
     *
     * @param integer $category_id
     * @return void
     */
    public function findVisibleTravelsByCategory(int $category_id)
    {
        return $this->createQueryBuilder('category')
            ->andWhere('category.id = :val')
            ->setParameter('val', $category_id)
            ->leftjoin('category.travels', 'travel')
            ->andWhere('travel.visibility = :visible')
            ->setParameter('visible', 1)
            ->addSelect('travel')
            ->getQuery()
            ->getResult()
        ;
    }

